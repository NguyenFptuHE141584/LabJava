    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!valid()) {
            return;
        }
        String ID = txtId.getText();
        String Fullname = txtFullname.getText();
        String Email = txtEmail.getText();
        String gender = rbMale.isSelected() ? "Male" : "Female";
        String Phone = txtPhone.getText();
        if (addNew) {
            Vector v = new Vector();
            v.add(ID);
            v.add(Fullname);
            v.add(gender);
            v.add(Email);
            v.add(Phone);
            data.add(v);
            addNew = false;
        } else {
            int pos = tblContact.getSelectedRow();
            Vector v = (Vector) data.get(pos);
            v.set(0, ID);
            v.set(1, Fullname);
            v.set(2, gender);
            v.set(3, Email);
            v.set(4, Phone);
        }
        tblContact.updateUI();
        changed = true;
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int pos = this.tblContact.getSelectedRow();
        if (pos >= 0) {
            String code = (String) (tblContact.getValueAt(pos, 0));
            if (JOptionPane.showConfirmDialog(this, "Delete the "
                    + code + "employee ?") == JOptionPane.OK_OPTION) {
                data.remove(pos);
                tblContact.updateUI();
                changed = true;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

