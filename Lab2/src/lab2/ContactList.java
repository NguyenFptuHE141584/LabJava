    private void loadData() {
        try {
            File file = new File("D:\\Java\\JavaLab\\Lab2\\src\\lab2\\Contact.txt");
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            String a;
            while ((a = br.readLine()) != null) {
                a = a.trim();
                if (a.length() > 0) {
                    StringTokenizer stk = new StringTokenizer(a, ";");
                    String id = stk.nextToken();
                    String fullName = stk.nextToken();
                    String gender = stk.nextToken();
                    String email = stk.nextToken();
                    String phone = stk.nextToken();
                    Vector<String> v = new Vector<String>();
                    v.add(id);
                    v.add(fullName);
                    v.add(gender);
                    v.add(email);
                    v.add(phone);
                    data.add(v);
                }
            }
            fr.close();
            br.close();

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, " File not found !");
        } catch (IOException e1) {
            JOptionPane.showMessageDialog(this, " File can't read !");
        }
    }

