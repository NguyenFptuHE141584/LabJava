    boolean valid() {
        // Check ID: 6 digit
        if (!txtId.getText().matches("^\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "ID is an integer with length 6 digits.");
            txtId.requestFocus();
            return false;
        }

        for (int i = 0; i < data.size(); i++) {
            Vector v = (Vector) data.get(i);
            if (txtId.getText().trim().equals(v.get(0))) {
                JOptionPane.showMessageDialog(this, "ID duplicated.");
                return false;
            }
        }

        // Check Fullname: not empty
        if (txtFullname.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Fullname is required.");
            txtFullname.requestFocus();
            return false;
        }
        if (!txtFullname.getText().trim().matches("^[^0-9]+$")) {
            JOptionPane.showMessageDialog(this, "Fullname is not contain digit.");
            txtFullname.requestFocus();
            return false;
        }

        // Check email: Ex - thopn3@fpt.edu.vn
        if (!txtEmail.getText().matches("^[\\w\\.]+@[\\w\\.]+\\.\\w{2,3}$")) {
            JOptionPane.showMessageDialog(this, "Email invalid. Ex: thopn3@fpt.edu.vn");
            txtEmail.requestFocus();
            return false;
        }

        // Check phone: is digit
        if (!txtPhone.getText().matches("^\\d{10}$")) {
            JOptionPane.showMessageDialog(this, "Phone is an integer with length 10 digits.");
            txtPhone.requestFocus();
            return false;
        }

        return true;
    }

